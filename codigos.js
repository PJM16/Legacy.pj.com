function loadCodigos(tipoCodigo, searchTerm = '') {
  //console.log('Cargando códigos para:', tipoCodigo);
  const codesContainer = document.getElementById('codesContainer');
  codesContainer.innerHTML = '';

  const codigos = codigosData[tipoCodigo];
  if (codigos) {
    codigos.forEach((codigo) => {
      if (codigo.codigo.toLowerCase().includes(searchTerm.toLowerCase())) {
        const codigoElement = document.createElement('div');
        codigoElement.classList.add('codigo-item');
        codigoElement.textContent = codigo.codigo;

        codigoElement.addEventListener('click', () => showSignificado(codigo));
        codesContainer.appendChild(codigoElement);
      }
    });
  }
}

function showSignificado(codigo) {
  //console.log('Mostrando significado para:', codigo.codigo);
  const significadoContainer = document.getElementById('significado');
  significadoContainer.innerHTML = `
    <p><strong>Código:</strong> ${codigo.codigo}</p>
    <p><strong>Significado:</strong> ${codigo.significado}</p>
  `;
}

const codeSelect = document.querySelector('select[name="code"]');
codeSelect.addEventListener('change', (event) => {
  const selectedTipoCodigo = event.target.value;
  loadCodigos(selectedTipoCodigo);
  showSignificado({ codigo: '', significado: '' }); // Limpiar el significado al cambiar el tipo
});

window.addEventListener('load', () => {
  const initialTipoCodigo = codeSelect.value;
  loadCodigos(initialTipoCodigo);
});

const searchInput = document.getElementById('searchInput');
searchInput.addEventListener('input', () => {
  const selectedTipoCodigo = codeSelect.value;
  const searchTerm = searchInput.value;
  loadCodigos(selectedTipoCodigo, searchTerm);
});

